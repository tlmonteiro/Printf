#include "ft_printf.h"

int	ft_putstr(char *s)
{
	if (*s == 0)
	{
		write(1, "(null)", 6);
		return ;
	}
	while (*s)
	{
		ft_putchar(*s);
		s++;
	}
	return (ft_strlen(s));
}
